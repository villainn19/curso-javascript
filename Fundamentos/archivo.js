//operadores de asignacion, incremento y decremento

let a = 1;

document.write('Valor de a: ', a);
document.write('<br>');
a++;// aumenta valor en 1
document.write('Valor de a: ', a);
document.write('<br>');
a+=5; //aumento variable
document.write('Valor de a: ', a);
document.write('<br>');
a--;
document.write('Valor de a: ', a);
document.write('<br>');
a-=3;
document.write('Valor de a: ', a);
document.write('<br>');
a*=2;
document.write('Valor de a: ', a);
document.write('<br>');
a/=3;
document.write('Valor de a: ', a);
document.write('<br>');
a**=3;
document.write('Valor de a: ', a);
